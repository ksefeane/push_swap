#include "libft.h"

char	*ft_itoa(int n)
{
	char	*a;
	int		i;
	long	x;
	long	y;

	i = 1;
	x = (long)n;
	y = (long)n;
	x *= (y < 0) ? -1 : 1;
	while (x > 9)
	{
		i++;
		x /= 10;
	}
	if (!(a = (y < 0) ? ft_strnew(++i) : ft_strnew(i)))
		return (0);
	x = (y < 0) ? 0 : -1;
	(y < 0) ? a[0] = '-' : 0;
	y *= (y < 0) ? -1 : 1;
	while (--i > x)
	{
		a[i] = (y % 10) + 48;
		y /= 10;
	}
	return (a);
}
