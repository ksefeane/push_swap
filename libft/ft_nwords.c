#include "libft.h"

int		ft_nwords(char const *s, char c)
{
	int		n;
	size_t	i;

	i = 0;
	n = 0;
	while (s[n])
	{
		if (s[n] != c && (s[n + 1] == c || s[n + 1] == '\0'))
			i++;
		n++;
	}
	return (i);
}
