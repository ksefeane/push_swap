#include "../../inc/push_swap.h"

void	ft_strxdel(char ***s)
{
	char	**a;
	int		i;

	a = *s;
	i = 0;
	if (!a)
		return ;
	while (a[i])
	{
		free(a[i]);
		i++;
	}
	free(a);
	*s = NULL;
}
