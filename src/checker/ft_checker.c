#include "../../inc/checker.h"

int		ft_checker(int ac, char **av)
{
	int		i;

	i = 1;
	while (i < ac)
	{
		ft_putendl(av[i]);
		i++;
	}
	ft_putendl("_");
	ft_putendl("a");
	return (1);
}
